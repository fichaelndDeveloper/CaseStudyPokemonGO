package Models;

public class Friendship {
    private int friendone;
    private int friendtwo;
    private int friendshipID;
    private int friendshipLVL;

    public int getFriendone() {
        return friendone;
    }

    public void setFriendone(int friendone) {
        this.friendone = friendone;
    }

    public int getFriendtwo() {
        return friendtwo;
    }

    public void setFriendtwo(int friendtwo) {
        this.friendtwo = friendtwo;
    }

    public int getFriendshipID() {
        return friendshipID;
    }

    public void setFriendshipID(int friendshipID) {
        this.friendshipID = friendshipID;
    }

    public int getFriendshipLVL() {
        return friendshipLVL;
    }

    public void setFriendshipLVL(int friendshipLVL) {
        this.friendshipLVL = friendshipLVL;
    }

}
